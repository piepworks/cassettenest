def env_var(key, default=None):
    '''
    Retrieves env vars and makes Python boolean replacements
    https://wellfire.co/learn/easier-12-factor-django/
    '''
    val = os.environ.get(key, default)
    if val == 'True' or val == '1':
        val = True
    elif val == 'False':
        val = False
    return val

